			<p><span class='name'>Belinda Close</span> is an artist and a proud Quandamooka woman. She comes from a long line of strong Quandamooka women – her apical line is from Grannie Nellie Ngari Kidgarie. She was also part of the last generation of children that grew up at One Mile on Minjerribah with the old people.</p>
			<p>“I remember the old people hunting parrots with whips and copper wire. They would hide in the ferns and catch those parrots – then everyone at One Mile would have parrot stew for dinner that night. I remember Mum and Pop setting traps and pulling the bandicoots out. I can’t remember a time when I wasn’t drawing but my first painting was the story of the ferns and the parrots. I also remember getting growled at by the old people because I had the parrots coming in from the wrong direction. The old people were very strict and let you know straight up if you didn’t get the story right! They scared us with stories of the Djundjeries to make sure we didn’t stray too far into either the water or bush.”</p>
			<p>“In winter we lived on the mullet and were spoon fed dugong oil to keep us strong. I always needed a teaspoon of honey to follow – I didn’t like the taste of dugong oil. Mum and the grannies would rub the dugong oil into our backs and chest. Wild horses roamed the island and all the roads were sand or dirt. Mum and the other women washed by hand over copper boilers on open fires. To keep me away from the fire they put me in charge of the chooks but I really loved those chickens and would let them out. The old people growled me for that – it meant no eggs for us.”</p>
			<p>All these images and symbols find their way into Belinda’s paintings.</p>
			<p>Belinda sees herself as an emerging professional artists and has been a part of group exhibitions. She dreams to one day have her own exhibition and continuing to share stories, through her art and in books to be put in our schools so our children; Indigenous and non-indigenous can learn, read, share, feel proud and connect with our real life, true grass roots history from Minjerribah-Quandamooka.</p>
			<p>She feels that if she does not share her stories about home/life on North Stradbroke Island (how almost perfect and so very spiritual and special life was back then), how sad it would be if they had to read about everyone else’s stories and culture and not their own.</p>
			<p>Belinda paints her country/island and the stories that belong to home and she wants to keep on painting because there is so much that she wishes to share and tell the future generations.</p>
			<p>“To me, it is all about leaving this for the next generation.”</p>
